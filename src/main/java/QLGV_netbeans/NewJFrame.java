    public boolean validateForm(){
        return !(txtMaGV.getText().isEmpty() || txtTenGV.getText().isEmpty() || txtNgaySinh.getText().isEmpty() || txtEmail.getText().isEmpty() || txtDiaChi.getText().isEmpty() || lblHinhAnh.getIcon() == null);
    }
    public GiangVien getModel() throws ParseException{
        GiangVien gv = new GiangVien();
        gv.setMaGV(txtMaGV.getText());
        gv.setTenGV(txtTenGV.getText());
        gv.setEmail(txtEmail.getText());
        boolean gt = false;
        if(rdNam.isSelected()){
            gt=true;
        }
        gv.setGioiTinh(gt);
        gv.setDiaChi(txtDiaChi.getText());
        gv.setNgaySinh(date_format.parse(txtNgaySinh.getText()));
        if(strHinhAnh == null) {
            gv.setHinhAnh("NO AVATAR");
        }else {
            gv.setHinhAnh(strHinhAnh);
        }
        return gv;
    }
