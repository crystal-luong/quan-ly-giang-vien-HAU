    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
         int selectedRowIndex = tbGiangVien.getSelectedRow();

    // Kiểm tra xem có dòng nào được chọn không
            if (selectedRowIndex == -1) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn một giảng viên để xóa.");
                return; // Nếu không có dòng nào được chọn, thoát khỏi hàm
            }

            // Xác nhận việc xóa thông tin giảng viên
            int option = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa thông tin giảng viên này?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
            if (option == JOptionPane.YES_OPTION) {
                // Xóa dòng được chọn từ bảng
                DefaultTableModel model = (DefaultTableModel) tbGiangVien.getModel();
                model.removeRow(selectedRowIndex);

                // Hiển thị thông báo hoặc thực hiện các hành động khác sau khi xóa thành công
                JOptionPane.showMessageDialog(this, "Đã xóa thông tin giảng viên.");
            }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tbGiangVien.getSelectedRow();

    // Kiểm tra xem người dùng đã chọn dòng nào trong bảng chưa
        if (selectedRowIndex == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một giảng viên để sửa thông tin.");
            return; // Nếu không có dòng nào được chọn, thoát khỏi hàm
        }

        // Lấy thông tin từ các trường dữ liệu trên giao diện
        String maGV = txtMaGV.getText();
        String tenGV = txtTenGV.getText();
        String ngaySinh = txtNgaySinh.getText();
        String gioiTinh = rdNam.isSelected() ? "NAM" : "NU";
        String email = txtEmail.getText();
        String diaChi = txtDiaChi.getText();

        // Lấy model từ bảng
        DefaultTableModel model = (DefaultTableModel) tbGiangVien.getModel();

        // Cập nhật thông tin giảng viên trong bảng
        model.setValueAt(maGV, selectedRowIndex, 0);
        model.setValueAt(tenGV, selectedRowIndex, 1);
        model.setValueAt(ngaySinh, selectedRowIndex, 2);
        model.setValueAt(gioiTinh, selectedRowIndex, 3);
        model.setValueAt(email, selectedRowIndex, 4);
        model.setValueAt(diaChi, selectedRowIndex, 5);

        // Hiển thị thông báo hoặc thực hiện các hành động khác sau khi sửa thành công
        JOptionPane.showMessageDialog(this, "Thông tin giảng viên đã được cập nhật.");
    }//GEN-LAST:event_btnSuaActionPerformed
