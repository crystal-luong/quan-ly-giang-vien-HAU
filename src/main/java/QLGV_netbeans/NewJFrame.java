    public void setModel(){
        int rowIndex = tbGiangVien.getSelectedRow(); // Lấy chỉ số của hàng được chọn
        if (rowIndex >= 0) {
            String maGV = tbGiangVien.getValueAt(rowIndex, 0).toString();
            String tenGV = tbGiangVien.getValueAt(rowIndex, 1).toString();
            String ngaySinh = tbGiangVien.getValueAt(rowIndex, 2).toString();
            String gioiTinh = tbGiangVien.getValueAt(rowIndex, 3).toString();
            String email = tbGiangVien.getValueAt(rowIndex, 4).toString();
            String diaChi = tbGiangVien.getValueAt(rowIndex, 5).toString();
            String hinhAnh = tbGiangVien.getValueAt(rowIndex, 6).toString();
            txtMaGV.setText(maGV);
            txtTenGV.setText(tenGV);
            txtNgaySinh.setText(ngaySinh);
            // Xử lý giới tính
            if (gioiTinh.equalsIgnoreCase("NAM")) {
                rdNam.setSelected(true);
            } else {
                rdNu.setSelected(true);
            }
            txtEmail.setText(email);
            txtDiaChi.setText(diaChi);

            // Placeholder cho txtNgaySinh
            txtNgaySinh.setForeground(Color.GRAY);
            txtNgaySinh.setText("Ngày sinh (dd/mm/yyyy)");
            txtNgaySinh.addFocusListener(new FocusListener() {
                @Override
                public void focusGained(FocusEvent e) {
                    if (txtNgaySinh.getText().equals("Ngày sinh (dd/mm/yyyy)")) {
                        txtNgaySinh.setText("");
                        txtNgaySinh.setForeground(Color.BLACK);
                    }
                }

                @Override
                public void focusLost(FocusEvent e) {
                    if (txtNgaySinh.getText().isEmpty()) {
                        txtNgaySinh.setForeground(Color.GRAY);
                        txtNgaySinh.setText("Ngày sinh (dd/mm/yyyy)");
                    }
                }
            });
        }
        //load hinh anh
            
            
    }
