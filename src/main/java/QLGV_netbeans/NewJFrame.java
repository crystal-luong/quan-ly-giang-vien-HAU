    public void fillDateTABLE() {
        DefaultTableModel model = (DefaultTableModel)tbGiangVien.getModel();
        model.setRowCount(0);
        for (GiangVien gv : dao.getAllGiangVien()) {
            Object rowData[] = new Object[7];
            rowData[0] = gv.getMaGV();
            rowData[1] = gv.getTenGV();
            rowData[2] = date_format.format(gv.getNgaySinh());
            rowData[3] = gv.isGioiTinh()?"NAM":"NU";
            rowData[4] = gv.getEmail();
            rowData[5] = gv.getDiaChi();
            rowData[6] = gv.getHinhAnh();
            model.addRow(rowData);
        }
    }
    
    
